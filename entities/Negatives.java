package entities;

public class Negatives {
	private int number;

	public Negatives() {
	}
	public Negatives(int number) {
		super();
		this.number = number;
	}
	public int numbers() {
		return number;
	}
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}

}
